class compare
{
	public static void main(String[] args)
	{
		System.out.println("program starts..");

		int a1 = 10;
		int b1 = 20;

		System.out.println(a1 < b1);
		System.out.println(a1 > b1);
		System.out.println(a1 == b1);
		System.out.println(a1 != b1);
		System.out.println(a1 <= b1);
		System.out.println(a1 >= b1);
    
		System.out.println("program ends..");	
	}
}