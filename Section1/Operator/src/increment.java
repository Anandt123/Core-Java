class increment
{
	public static void main(String[] args)
	{
		System.out.println("program starts..");

		int x1;
		int y1;

		x1 = 10;
		y1 = 10;

		System.out.println("x1 : "+x1);
  		x1++;
    	System.out.println("x1 : "+x1);
    	System.out.println("y1 : "+y1);
    	y1++;
    	System.out.println("y1 : "+y1);
    
		System.out.println("program ends..");	
	}
}